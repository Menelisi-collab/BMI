    for i in range(18.4, 31.0):
        total = random.float(18.4,31.0)
        return bmi(total)

    if bmi <= 8.5:
        return "underweight"
    elif bmi<= 25.0:
        return "Normal"
    elif bmi <= 30.0:
        return "Overweight"
    else:
        return "Obese"
